     private void consultarVenda(){
        String sql = "Select * from tblvendas where idvendas=?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1,idvenda.getText());
            rs = pst.executeQuery();
            if (rs.next()){
               produto.setText(rs.getString(2));
               valor.setText(rs.getString(3));
               qntvenda.setText(rs.getString(4));
               valortotal.setText(rs.getString(5));
               
               
            } else {
                JOptionPane.showMessageDialog(null,"Venda nÃ£o localizada");
                limpaForm();
               
            
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);        }
        
        
    }
       
    
        
     
   
    public void validaCampo(){
    
   
    if ((idvenda.getText().isEmpty()) || produto.getText().isEmpty() || valor.getText().isEmpty() || qntvenda.getText().isEmpty()|| valortotal.getText().isEmpty() ){
    JOptionPane.showMessageDialog(null,"Preencha todos os campos!");
}
}
    
    
    private void CalcularVenda(){
      
      
      double total = Double.parseDouble(qntvenda.getText()) * Double.parseDouble(valor.getText()) ;
        valortotal.setText(String.valueOf(total));
      
    }
    private void AdicionarVenda(){
    
      String sql = "insert into tblvendas (idvendas, produto,valor,quantidade,total) values (?,?,?,?,?)";
      try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1,idvenda.getText());
            pst.setString(2,produto.getText());
            pst.setString(3,valor.getText());
             pst.setString(4,qntvenda.getText());
           pst.setString(5,valortotal.getText());
           validaCampo();
            
            //Iserir os dados na tabela com
            int adicionado = pst.executeUpdate();
            
            if (adicionado>0) {
                JOptionPane.showMessageDialog(null,"Venda Cadastrada");
                
            } else {
                JOptionPane.showMessageDialog(null,"ERRO404!! ao cadastrar venda!");
            }
        } catch (Exception e) {
            
        }
    
    
    
    }
